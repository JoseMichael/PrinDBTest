
public class ReadDiskPageAddress 
{
	private String PageNumber;
	private String LastBytePointerLocation;
	
	public String getPageNumber() {
		return PageNumber;
	}
	public void setPageNumber(String pageNumber) {
		PageNumber = pageNumber;
	}
	public String getLastBytePointerLocation() {
		return LastBytePointerLocation;
	}
	public void setLastBytePointerLocation(String lastBytePointerLocation) {
		LastBytePointerLocation = lastBytePointerLocation;
	}
}
